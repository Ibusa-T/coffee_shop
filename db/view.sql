CREATE VIEW VISIT  AS SELECT
    CONSUMER.CONSUMER_CODE AS 顧客コード
   ,CONSUMER.CONSUMER_NAME AS 顧客名
   ,MAX(DENSE_RANK() OVER W) AS 購入回数 
FROM CONSUMER
JOIN SLIP 
ON CONSUMER.CONSUMER_CODE = SLIP.CONSUMER_CODE
GROUP BY CONSUMER.CONSUMER_CODE,CONSUMER.CONSUMER_NAME,SL.ORDER_DATE
WINDOW W AS (PARTITION BY CONSUMER.CONSUMER_CODE ORDER BY SL.ORDER_DATE) ;